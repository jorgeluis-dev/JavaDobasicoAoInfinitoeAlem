public class ExemploInt {
    public static void main(String[] args) {
        int idade = 30;
        int quantidadeAmigos = 5;
        int totalAnosEstudo = 12;

        int soma = idade + quantidadeAmigos;
        int subtracao = quantidadeAmigos - totalAnosEstudo;
        int multiplicacao = idade * totalAnosEstudo;
        int divisao = idade / quantidadeAmigos;

        System.out.println("Idade: " + idade);
        System.out.println("Quantidade de amigos: " + quantidadeAmigos);
        System.out.println("Total de anos de estudo: " + totalAnosEstudo);
        System.out.println("Soma: " + soma);
        System.out.println("Subtração: " + subtracao);
        System.out.println("Multiplicação: " + multiplicacao);
        System.out.println("Divisão: " + divisao);
    }
}
