public class ExemploDouble {
    public static void main(String[] args) {
        double salarioMensal = 2500.50;
        double bonusAnual = 1200.75;

        double salarioTotal = salarioMensal * 12 + bonusAnual;

        System.out.println("Salário Mensal: R$ " + salarioMensal);
        System.out.println("Bônus Anual: R$ " + bonusAnual);
        System.out.println("Salário Total: R$ " + salarioTotal);
    }
}
